	private static void fillSet(HashSet<String> hs, String[] invalidArr)
	{
		if (hs == null)
		{
			hs = new HashSet<String>();
		}
		for (int i = 0; i < invalidArr.length; i++)
		{
			hs.add(invalidArr[i]);
		}
	}

	private static void fillSet(HashSet<Character> hs, String str)
	{
		if (hs == null)
		{
			hs = new HashSet<Character>();
		}
		for (int i = 0; i < str.length(); i++)
		{
			hs.add(str.charAt(i));
		}
	}

	private static void fillMap(HashMap<Character, Character> keyValueMap, String keyStr,
			String valueStr)
	{
		if (keyValueMap == null)
		{
			keyValueMap = new HashMap<Character, Character>();
		}
		for (int i = 0; i < keyStr.length(); i++)
		{
			keyValueMap.put(keyStr.charAt(i), valueStr.charAt(i));
		}
	}

