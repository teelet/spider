	public String Change(String str, boolean flag)
	{
		if (flag == true)
		{
			return toShort(str);
		} else
		{
			return toLong(str);
		}
	}

	// 是否为常用简体字
	public static boolean isCommonShort(char word)
	{
		if (commonShortCharSet.contains(word) || secondCommonShortCharSet.contains(word))
		{
			return true;
		} else
		{
			return false;
		}
	}

	// 简转繁体
	public static String toLong(String str)
	{
		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < str.length(); i++)
		{
			char curChar = str.charAt(i);
			if (shortLongMap.containsKey(curChar))
			{
				sb.append(shortLongMap.get(curChar));
			} else
			{
				sb.append(curChar);
			}
		}
		return sb.toString();
	}

	// 繁体转简
	public static String toShort(String str)
	{
		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < str.length(); i++)
		{
			char curChar = str.charAt(i);
			if (longShortMap.containsKey(curChar))
			{
				sb.append(longShortMap.get(curChar));
			} else
			{
				sb.append(curChar);
			}
		}
		return sb.toString();
	}

